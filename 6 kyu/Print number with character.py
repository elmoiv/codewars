ns = [
    [' **** ', '**  **', '**  **', '**  **', '**  **', ' **** '],
    ['  **  ', ' ***  ', '* **  ', '  **  ', '  **  ', '******'],
    [' **** ', '**  **', '   ** ', '  **  ', ' **   ', '******'],
    [' **** ', '**  **', '   ** ', '   ** ', '**  **', ' **** '],
    ['**  **', '**  **', '**  **', ' *****', '    **', '    **'],
    ['******', '**    ', '***** ', '    **', '    **', '***** '],
    ['   ** ', '  **  ', ' **** ', '**  **', '**  **', ' **** '],
    ['******', '**  **', '   ** ', '  **  ', ' **   ', ' **   '],
    [' **** ', '**  **', ' **** ', ' **** ', '**  **', ' **** '],
    [' **** ', '**  **', '**  **', ' **** ', '  **  ', ' **   ']
]

def print_number(n, c):
    n, l = str(n).rjust(5, '0'), [c * 40, c + ' ' * 38 + c]
    x = '\n'.join(f"{c}  {' '.join(ns[int(j)][i].replace('*', c) for j in n)}  {c}" for i in range(6))
    return '\n'.join(['\n'.join(l),x,'\n'.join(l[::-1])])
